/**
 * Translate the passed word into the selected Language.
 * Additional arguments can be passed, which are inserted into the
 * translated strings where the markers %1, %2, ... occur. %n indicates
 * that the n-th argument will be inserted at that point.
 * @param {Object} word
 * @return translation
 */
GQB.translate = function(word){
	var foundTransStr = "[Translation missing]";
	if(!this.translations[word]) return foundTransStr;

	if(this.translations[word][this.currLang] == undefined){
		for(lang in this.translations[word]){
			foundTransStr = this.translations[word][lang];
			break;
		}
	} else {
		foundTransStr = this.translations[word][this.currLang];
	}
	var extraArgCounter = 1;
	
	while (arguments[extraArgCounter] != undefined) {
		
		arguments[extraArgCounter] = arguments[extraArgCounter] + ""; // cast to string
		var placeholder = "%"+extraArgCounter;
		// strip "%" out of the argument, just in case...
		while (arguments[extraArgCounter].indexOf("%") != -1) {
			arguments[extraArgCounter] = arguments[extraArgCounter].replace("%", "PCT");
		}
		while (foundTransStr.indexOf(placeholder) != -1) {
			foundTransStr = foundTransStr.replace(placeholder,arguments[extraArgCounter]);
		}
		extraArgCounter++;
	}
	return unescape(foundTransStr);
};
